export default function BottomNav() {
  const pathname = usePathname();
  const [isStandalone, setIsStandalone] = useState(false);

  useEffect(() => {
    // Detect PWA standalone mode (no Safari toolbar)
    const mq = window.matchMedia("(display-mode: standalone)");
    setIsStandalone(mq.matches || (navigator as any).standalone === true);
    const handler = (e: MediaQueryListEvent) => setIsStandalone(e.matches);
    mq.addEventListener("change", handler);
    return () => mq.removeEventListener("change", handler);
  }, []);

  return (
    <nav
      className="fixed bottom-0 left-0 right-0 z-[1000] md:hidden"
      style={{
        background: "rgba(255,255,255,0.82)",
        backdropFilter: "blur(20px) saturate(180%)",
        WebkitBackdropFilter: "blur(20px) saturate(180%)",
        borderTop: "1px solid rgba(0,0,0,0.06)",
        /* In standalone PWA — only safe area for Home Indicator.
           In browser — extra padding to sit above Safari's bottom toolbar. */
        paddingBottom: isStandalone
          ? "env(safe-area-inset-bottom, 0px)"
          : "calc(env(safe-area-inset-bottom, 0px) + 8px)",
      }}
    >
      <div className="flex h-[52px] items-stretch justify-around">
        {tabs.map((tab) => {
          const isActive =
            tab.href === "/"
              ? pathname === "/"
              : pathname.startsWith(tab.href);

          return (
            <Link
              key={tab.href}
              href={tab.href}
              className={`flex min-w-[64px] flex-col items-center justify-center gap-0.5 transition-colors duration-200 ${
                isActive ? "text-terra-500" : "text-neutral-400"
              }`}
            >
              {tab.icon(isActive)}
              <span className="text-[10px] font-medium leading-tight">
                {tab.label}
              </span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}
